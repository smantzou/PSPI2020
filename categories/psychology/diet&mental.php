                    <p class="big">There is research to suggest that what we eat may affect not just our physical health, but also our mental health and wellbeing.</p>
                    <p class="small">
                        Eating well (i.e. a well-balanced diet rich in vegetables and nutrients) may be associated with feelings of wellbeing. One 2014 study found high levels of wellbeing were reported by individuals who ate more fruit and vegetables. 
                    </p>
                    <p class="small">
                        A recent study found that a Mediterranean-style diet (a diet high in vegetables, fruits, legumes, nuts, beans, cereals, grains, fish, and unsaturated fats such as olive oil.) supplemented with fish oil led to a reduction in depression among participants, which was sustained six months after the intervention.
                    </p>
                    <p class="small">
                        The importance of good nutritional intake at an early age is explored in multiple studies, including a systematic review in 2014, which found that a poor diet (with high levels of saturated fat, refined carbohydrates and processed food products) is linked to poorer mental health in children and adolescents.
                    </p>
                    <p class="small">
                        However, there are a range of inequalities that can contribute to the development of mental health problems, and how these factors interact with each other to affect mental health can be complex. Factors such as poorer physical health, and living in poverty, or deprived communities, have been found to be associated with poorer mental health and wellbeing.  Both these inequality factors have also been shown to have a complex relationship with poor nutrition.
                    </p>
                    <p class="small">
                        Experience of a mental health problem may also be associated with poorer diet and physical health. There have been efforts to close the ‘mortality gap’ for people with severe mental health problems, who on average tend to die 10 to 25 years earlier than the general population.5  A number of factors may contribute to this premature mortality, including dietary and nutritional factors, among other things.
                    </p>
                    <p class="small">
                        Poor nutrition can lead to physical health problems such as obesity, though there are a number of demographic variables that could affect the direction and/or strength of the association with mental health including severity of obesity, socioeconomic status and level of education, gender, age and ethnicity.
                    </p>
                    <p class="small">
                        The relationship between obesity and mental health problems is complex.  Results from a 2010 systematic review found two-way associations between depression and obesity, finding that people who were obese had a 55% increased risk of developing depression over time, whereas people experiencing depression had a 58% increased risk of becoming obese.
                    </p>
                    <p class="small">
                        While a healthy diet can help recovery, it should sit alongside other treatments recommended by your doctor. 
                    </p>                
